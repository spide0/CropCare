<div id="Introduction" class="container">
  <div class="card-deck">
    <div class="card">
      <img src="timing.jpg" class="card-img-top" alt="Timing Issues">
      <div class="card-body">
        <h5 class="card-title">Timing Issues</h5>
        <p class="card-text">Harvesting crops too early or too late can result in significant losses.Timing in harvesting is critical to minimize post-harvest losses, 
          as both premature and delayed harvesting can significantly impact the quality and quantity of produce. 
          Harvesting too early often results in immature crops with lower nutritional value, poor taste, and reduced market appeal, leading to decreased prices. 
          Conversely, delayed harvesting can cause over-ripening, making produce more susceptible to spoilage, physical damage during handling, and pest infestations. 
          These timing issues are particularly pronounced in perishable crops like fruits and vegetables, where even minor delays can lead to rapid degradation. 
          Proper timing, based on crop maturity indicators and environmental conditions, ensures optimal yield and quality.
        </p>
      </div>
    </div>
    <div class="card">
      <img src="handling.jpg" class="card-img-top"  alt="Improper Handling">
      <div class="card-body">
        <h5 class="card-title">Improper Handling</h5>
        <p class="card-text">Improper handling during harvesting can lead to significant physical damage to crops, resulting in reduced market value and increased waste. 
          Rough handling, such as dropping or throwing produce, can cause bruising, cuts, or crushing, making crops more susceptible to microbial infections and spoilage. 
          The use of unsuitable tools, such as dull knives or inappropriate harvesting equipment, can further exacerbate these issues by causing uneven cuts or damage to the produce's skin. 
          This is particularly problematic for delicate fruits and vegetables, which require careful handling to maintain their quality. 
          Training workers in proper harvesting techniques and using appropriate tools can mitigate these losses.
        </p>
      </div>
    </div>
    <div class="card">
      <img src="weather.jpg" class="card-img-top" alt="Weather Conditions">
      <div class="card-body">
        <h5 class="card-title">Effects of Weather Conditions</h5>
        <p class="card-text">Unpredictable weather conditions during harvest seasons are a major factor contributing to post-harvest losses. 
          Excessive rainfall can delay harvesting, leading to crop spoilage, mold growth, and reduced quality due to waterlogging. 
          Similarly, unexpected frost can damage ripened crops, while high temperatures and strong winds may cause dehydration, shattering, or physical damage to produce. 
          These weather extremes are especially challenging for crops left exposed in fields, where immediate harvesting or protective measures are not feasible. 
          To reduce losses, farmers can implement strategies such as monitoring weather forecasts, scheduling harvests accordingly, 
          and using protective coverings or temporary storage solutions during adverse conditions.
        </p>
      </div>
    </div>
