<div id="Impact" class="container">
  <div class="card">
    <h5 class="card-header">Impact of Harvest Loss</h5>
    <div class="card-body">
      <h5 class="card-title">Economic and Environmental Effects</h5>
      <p class="card-text">Harvest loss significantly impacts both the economy and the environment, creating challenges that extend beyond the immediate farming community. 
        Economically, these losses translate into reduced income for farmers, who bear the brunt of wasted effort, resources, and investment. 
        Crops lost during harvesting mean fewer goods available for sale, lowering profits and potentially destabilizing local food supply chains. 
        For small-scale farmers, who often operate with tight margins, such losses can lead to financial instability, difficulty in accessing future resources, 
        and an increased likelihood of poverty. Harvest loss also disrupts downstream industries, including food processing, packaging, and retail, further exacerbating economic challenges.
        <br>Environmentally, harvest losses contribute to a heightened ecological footprint. The resources used to grow the crops, including water, fertilizers, pesticides, and energy, 
        are wasted when crops are not properly harvested. This inefficiency results in the unnecessary depletion of natural resources, while the decomposition of unused produce generates 
        greenhouse gases like methane, contributing to climate change. Moreover, increased demand due to reduced supply can drive the expansion of agricultural land, leading to deforestation,
        habitat loss, and biodiversity decline. These environmental effects are magnified by the waste of inputs such as fuel used during planting, harvesting, and transportation processes, 
        all of which contribute to a growing carbon footprint. 
        <br>Addressing harvest losses is crucial for achieving sustainable agriculture and food security. 
        Implementing improved harvesting techniques, adopting better tools and machinery, and providing farmers with training can minimize these losses. 
        Additionally, the development of post-harvest storage facilities and processing options can reduce the waste of crops that are not immediately marketable. 
        By addressing both the economic and environmental impacts, stakeholders can create more resilient agricultural systems that support farmers 
        livelihoods while reducing the strain on the planet's ecosystems.
      </p>
